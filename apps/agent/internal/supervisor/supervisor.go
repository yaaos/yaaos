// Package supervisor implements the long-poll worker loop, identity
// exchange, command routing, heartbeat, and disk-janitor responsibilities
// of the agent's `supervisor` subcommand.
//
// Phase 6 ships the skeleton:
//   - identity exchange against the backend's placeholder verifier
//   - N concurrent claim-loop workers (`Config.Concurrency`)
//   - heartbeat loop reporting an in-memory workspace inventory
//   - command-routing stub that emits a `completed_success` event for any
//     command kind (real workspace spawning + Claude Code invocation
//     lands in a follow-on iteration)
//   - graceful Stop via context cancellation
//
// Real responsibilities deferred to follow-on iterations:
//   - per-workspace OS-process spawning + IPC + cleanup
//   - disk janitor scanning /var/agent/workspaces/
//   - wall-clock timeout enforcement per AgentCommand
//   - OTel SDK + traceparent extraction → child spans
//   - startup reconciliation that surfaces orphans in the first heartbeat
package supervisor

import (
	"context"
	"fmt"
	"sync"
	"time"

	"github.com/yaaos/agent/internal/protocol"
)

// Config carries the supervisor's runtime knobs.
type Config struct {
	BaseURL           string        // backend root, e.g. "https://yaaos.example.com"
	AgentPodID        string        // generated by the pod at startup, persisted locally
	Version           string        // agent binary version (semver)
	SignedSTSRequest  string        // SigV4-signed STS payload (Phase 7); any non-empty string passes the placeholder verifier
	Concurrency       int           // claim-loop workers; defaults to 4
	HeartbeatInterval time.Duration // defaults to 30s
	ClaimWaitSeconds  int           // long-poll horizon per claim; defaults to 30
}

// Logger is the minimal logging surface the supervisor needs. Real
// integrations plug structlog-like loggers in via this interface; tests
// pass a no-op.
type Logger interface {
	Info(msg string, kv ...any)
	Warn(msg string, kv ...any)
	Error(msg string, kv ...any)
}

type nullLogger struct{}

func (nullLogger) Info(string, ...any)  {}
func (nullLogger) Warn(string, ...any)  {}
func (nullLogger) Error(string, ...any) {}

// Supervisor wires the protocol client into the claim/heartbeat loops.
// Construct with New, then Run blocks until ctx is cancelled.
type Supervisor struct {
	cfg     Config
	client  *protocol.Client
	log     Logger
	agentID string

	// In-memory workspace inventory the heartbeat reports. Phase 6
	// foundations: every command we route gets an entry while it executes;
	// the entry is dropped after the terminal event is reported. The full
	// implementation tracks real OS processes.
	mu        sync.Mutex
	inventory map[string]protocol.HeartbeatWorkspaceEntry
}

// New constructs a Supervisor. The client is wired but identity hasn't
// been exchanged yet — call Run.
func New(cfg Config, client *protocol.Client, log Logger) *Supervisor {
	if log == nil {
		log = nullLogger{}
	}
	if cfg.Concurrency <= 0 {
		cfg.Concurrency = 4
	}
	if cfg.HeartbeatInterval <= 0 {
		cfg.HeartbeatInterval = 30 * time.Second
	}
	if cfg.ClaimWaitSeconds <= 0 {
		cfg.ClaimWaitSeconds = 30
	}
	return &Supervisor{
		cfg:       cfg,
		client:    client,
		log:       log,
		inventory: make(map[string]protocol.HeartbeatWorkspaceEntry),
	}
}

// Run exchanges identity and starts the claim + heartbeat goroutines.
// Blocks until ctx is cancelled or identity exchange fails fatally.
func (s *Supervisor) Run(ctx context.Context) error {
	resp, err := s.exchangeIdentity(ctx)
	if err != nil {
		return fmt.Errorf("identity exchange: %w", err)
	}
	s.agentID = resp.AgentID
	s.client.SetBearer(resp.Bearer)
	s.log.Info("supervisor.identity_exchanged", "agent_id", s.agentID)

	var wg sync.WaitGroup
	for i := 0; i < s.cfg.Concurrency; i++ {
		wg.Add(1)
		go func(workerNum int) {
			defer wg.Done()
			s.claimLoop(ctx, workerNum)
		}(i)
	}
	wg.Add(1)
	go func() {
		defer wg.Done()
		s.heartbeatLoop(ctx)
	}()
	wg.Wait()
	return nil
}

func (s *Supervisor) exchangeIdentity(ctx context.Context) (*protocol.IdentityExchangeResponse, error) {
	return s.client.ExchangeIdentity(ctx, protocol.IdentityExchangeRequest{
		AgentPodID:    s.cfg.AgentPodID,
		Version:       s.cfg.Version,
		SignedRequest: s.cfg.SignedSTSRequest,
	})
}

// claimLoop runs one long-poll worker. On a successful claim it dispatches
// the command via routeCommand and emits the terminal event. On ErrNoCommand
// (204) it re-arms; on ctx cancellation it exits.
func (s *Supervisor) claimLoop(ctx context.Context, workerNum int) {
	for {
		if ctx.Err() != nil {
			return
		}
		cmd, err := s.client.ClaimCommand(ctx, s.agentID, protocol.ClaimRequest{
			WaitSeconds: s.cfg.ClaimWaitSeconds,
		})
		if err != nil {
			if err == protocol.ErrNoCommand {
				continue
			}
			if ctx.Err() != nil {
				return
			}
			s.log.Warn("supervisor.claim_error", "worker", workerNum, "err", err.Error())
			// Backoff before retrying so a broken backend doesn't tight-loop.
			select {
			case <-ctx.Done():
				return
			case <-time.After(2 * time.Second):
			}
			continue
		}
		s.routeCommand(ctx, cmd)
	}
}

// heartbeatLoop reports liveness + workspace inventory on the configured
// interval. The reconciliation response (`forgotten_workspaces`) is logged;
// applying it to real OS processes lands in the follow-on iteration.
func (s *Supervisor) heartbeatLoop(ctx context.Context) {
	ticker := time.NewTicker(s.cfg.HeartbeatInterval)
	defer ticker.Stop()
	for {
		select {
		case <-ctx.Done():
			return
		case <-ticker.C:
			s.sendHeartbeat(ctx)
		}
	}
}

func (s *Supervisor) sendHeartbeat(ctx context.Context) {
	resp, err := s.client.Heartbeat(ctx, s.agentID, protocol.HeartbeatRequest{
		ReportedAt: time.Now().UTC(),
		Workspaces: s.snapshotInventory(),
	})
	if err != nil {
		s.log.Warn("supervisor.heartbeat_error", "err", err.Error())
		return
	}
	if len(resp.ForgottenWorkspaces) > 0 {
		s.log.Info("supervisor.heartbeat_reconciled",
			"forgotten_count", len(resp.ForgottenWorkspaces))
	}
}

// routeCommand is the Phase 6 stub. Each branch of the discriminator
// would normally drive a workspace OS process; for now we just emit the
// terminal event so the backend's workflow engine can advance. The full
// implementation lands alongside the workspace subcommand work.
func (s *Supervisor) routeCommand(ctx context.Context, cmd *protocol.AgentCommand) {
	header := cmd.Header()
	s.recordInventory(header.WorkspaceID, header.CommandID, "running")
	defer s.clearInventory(header.WorkspaceID)

	event := protocol.AgentEvent{
		CommandID:    header.CommandID,
		Kind:         protocol.EventCompletedSuccess,
		OutcomeLabel: "success",
		Outputs:      map[string]any{"workspace_id": header.WorkspaceID, "stubbed": true},
		Traceparent:  header.Traceparent,
		ReportedAt:   time.Now().UTC(),
	}
	if err := s.client.PostCommandEvent(ctx, header.CommandID, event); err != nil {
		if err == protocol.ErrStaleClaim {
			s.log.Info("supervisor.event_stale_claim", "command_id", header.CommandID)
			return
		}
		s.log.Warn("supervisor.event_post_failed", "command_id", header.CommandID, "err", err.Error())
	}
}

func (s *Supervisor) recordInventory(workspaceID, commandID, status string) {
	if workspaceID == "" {
		return
	}
	s.mu.Lock()
	defer s.mu.Unlock()
	s.inventory[workspaceID] = protocol.HeartbeatWorkspaceEntry{
		WorkspaceID:      workspaceID,
		Status:           status,
		CurrentCommandID: commandID,
	}
}

func (s *Supervisor) clearInventory(workspaceID string) {
	s.mu.Lock()
	defer s.mu.Unlock()
	delete(s.inventory, workspaceID)
}

func (s *Supervisor) snapshotInventory() []protocol.HeartbeatWorkspaceEntry {
	s.mu.Lock()
	defer s.mu.Unlock()
	out := make([]protocol.HeartbeatWorkspaceEntry, 0, len(s.inventory))
	for _, v := range s.inventory {
		out = append(out, v)
	}
	return out
}
